    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String productName = jTextField1.getText();
        try {
            Connection connection = ConnectionProvider.getCon();
            Statement statement = connection.createStatement();
            ResultSet getDatabaseInfo = statement.executeQuery("select * from AddNewProduct where productName='"+productName+"'");
            if (getDatabaseInfo.next()) {
                jTextField2.setText(getDatabaseInfo.getString(2));
                jTextField3.setText(getDatabaseInfo.getString(3));
                jTextField4.setText(getDatabaseInfo.getString(4));
                jTextField5.setText(getDatabaseInfo.getString(5));
                jTextField6.setText(getDatabaseInfo.getString(6));

                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
                jTextField3.setEditable(false);
                jTextField4.setEditable(false);
                jTextField5.setEditable(false);
                jTextField6.setEditable(false);

            }
            else  {
                JOptionPane.showMessageDialog(null,"Product Name does not exist!!!");
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

