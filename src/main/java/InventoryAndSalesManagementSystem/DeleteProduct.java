    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String productName= jTextField2.getText();
        int deleteProduct = JOptionPane.showConfirmDialog(null,"Do you really want to delete "+productName+" from database?","Select",JOptionPane.YES_NO_OPTION);
        if (deleteProduct==0) {
            try {
            Connection connection = ConnectionProvider.getCon();
            Statement statement = connection.createStatement();
            statement.executeUpdate("Delete from AddNewProduct where productName='"+productName+"'");
            setVisible(false);
            new DeleteProduct().setVisible(true);
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

